package pep.bp.model;

/**
 *
 * @author Thinkpad
 */
public class DataItem {
    private String dataItemCode;
    private String dataItemName;
    private String dataItemUnit;
    private String dataItemFormat;
    private String dataItemValue_Type;
    private String dataItemValue_Code;

    /**
     * @return the dataItemCode
     */
    public String getDataItemCode() {
        return dataItemCode;
    }

    /**
     * @param dataItemCode the dataItemCode to set
     */
    public void setDataItemCode(String dataItemCode) {
        this.dataItemCode = dataItemCode;
    }

    /**
     * @return the dataItemName
     */
    public String getDataItemName() {
        return dataItemName;
    }

    /**
     * @param dataItemName the dataItemName to set
     */
    public void setDataItemName(String dataItemName) {
        this.dataItemName = dataItemName;
    }

    /**
     * @return the dataItemUnit
     */
    public String getDataItemUnit() {
        return dataItemUnit;
    }

    /**
     * @param dataItemUnit the dataItemUnit to set
     */
    public void setDataItemUnit(String dataItemUnit) {
        this.dataItemUnit = dataItemUnit;
    }

    /**
     * @return the dataItemFormat
     */
    public String getDataItemFormat() {
        return dataItemFormat;
    }

    /**
     * @param dataItemFormat the dataItemFormat to set
     */
    public void setDataItemFormat(String dataItemFormat) {
        this.dataItemFormat = dataItemFormat;
    }

    /**
     * @return the dataItemValue_Type
     */
    public String getDataItemValue_Type() {
        return dataItemValue_Type;
    }

    /**
     * @param dataItemValue_Type the dataItemValue_Type to set
     */
    public void setDataItemValue_Type(String dataItemValue_Type) {
        this.dataItemValue_Type = dataItemValue_Type;
    }

    /**
     * @return the dataItemValue_Code
     */
    public String getDataItemValue_Code() {
        return dataItemValue_Code;
    }

    /**
     * @param dataItemValue_Code the dataItemValue_Code to set
     */
    public void setDataItemValue_Code(String dataItemValue_Code) {
        this.dataItemValue_Code = dataItemValue_Code;
    }
}
