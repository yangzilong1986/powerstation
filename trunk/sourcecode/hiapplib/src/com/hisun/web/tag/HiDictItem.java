 package com.hisun.web.tag;
 
 public class HiDictItem
 {
   public String name;
   public String type;
   public int length;
   public int size;
 
   public String getName()
   {
     return this.name; }
 
   public void setName(String name) {
     this.name = name; }
 
   public String getType() {
     return this.type; }
 
   public void setType(String type) {
     this.type = type; }
 
   public int getLength() {
     return this.length; }
 
   public void setLength(int length) {
     this.length = length;
   }
 
   public int getSize() {
     return this.size;
   }
 
   public void setSize(int size) {
     this.size = size;
   }
 }