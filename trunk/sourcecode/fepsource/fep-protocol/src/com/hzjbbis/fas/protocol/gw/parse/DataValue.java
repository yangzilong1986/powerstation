package com.hzjbbis.fas.protocol.gw.parse;

public class DataValue {
    private String value;
    private int len;

    public String getValue() {
        return this.value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public int getLen() {
        return this.len;
    }

    public void setLen(int len) {
        this.len = len;
    }
}